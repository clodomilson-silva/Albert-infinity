  static String translate(String text) {
    if (text.isEmpty) return text;

    String translatedText = text.toLowerCase();

    // Aplicar traduções palavra por palavra
    for (final entry in _translations.entries) {
      // Usar regex para garantir que apenas palavras completas sejam substituídas
      final regex = RegExp(
        r'\b' + RegExp.escape(entry.key) + r'\b',
        caseSensitive: false,
      );
      translatedText = translatedText.replaceAll(regex, entry.value);
    }

    // Capitalizar primeira letra
    if (translatedText.isNotEmpty) {
      translatedText =
          translatedText[0].toUpperCase() + translatedText.substring(1);
    }

    return translatedText;
  }

  static String translateDescription(String description) {
    if (description.isEmpty) return description;

    // Dividir em frases para manter a estrutura
    List<String> sentences = description.split('. ');
    List<String> translatedSentences = [];

    for (String sentence in sentences) {
      String translatedSentence = translate(sentence.trim());
      if (translatedSentence.isNotEmpty) {
        translatedSentences.add(translatedSentence);
      }
    }

    return translatedSentences.join('. ');
  }
